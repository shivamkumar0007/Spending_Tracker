    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try{
            java.util.Date dt=date.getDate();
            String s1=amount.getText();
            String s2=(String)category_list.getSelectedItem();
            if(dt!=null && !s1.equals("")&&!s2.equals("")){
                java.sql.Date d=new java.sql.Date(dt.getTime());
                    int am=Integer.parseInt(s1);
                    db.dbConnect.st.executeUpdate("insert into spendings (category,s_date,amount) values('"+s2+"','"+d+"',"+am+")");
                    JOptionPane.showMessageDialog(null,"Expense Added Successfully");
                    getEntries();
            }else{
                JOptionPane.showMessageDialog(null,"Please fill all the details properly");
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

