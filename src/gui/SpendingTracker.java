    private void displayCategory(){
        try{
            category_list.removeAllItems();
            ResultSet rs=db.dbConnect.st.executeQuery("select * from category_info");
            while(rs.next()){
                category_list.addItem(rs.getString("category"));
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null,ex);
        }
    }
    private void getEntries(){
        int tam=0;
        try{
            javax.swing.table.DefaultTableModel dtm=(javax.swing.table.DefaultTableModel)table.getModel();
            int rows=dtm.getRowCount();
            while(rows--!=0){
                dtm.removeRow(0);
            }
            java.time.LocalDate cd=java.time.LocalDate.now();
            java.time.LocalDate bd=cd.minusDays(20);
            ResultSet rs=db.dbConnect.st.executeQuery("select * from spendings where s_date<='"+cd+"'and s_date>='"+bd+"'");
            while(rs.next()){
                   Object o[]={rs.getInt("sid"),rs.getString("category"),rs.getDate("s_date"),rs.getString("amount")};
                   dtm.addRow(o);
                   tam=tam+Integer.parseInt(rs.getString("amount"));
            }
            String res=tam+"";
            total_amount.setText(res);
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null,ex);
        }
    }
